#include<stdio.h>

int main()
{
	float x, y, z;
	x = 155.423, y = 20.787, z = 50.34;
	printf("x : %.3f, y : %.3f, z : %.2f\n", x, y, z);
	printf("x+y+z = %.3f\n", x + y + z);
	printf("x-y-z = %.3f\n", x - y - z);
	printf("x/y/z = %.3f\n", x / y / z);
	printf("(x*y)/z = %.3f\n", (x * y) / z);
	
	return 0;
}